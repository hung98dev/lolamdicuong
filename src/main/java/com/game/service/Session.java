package com.game.service;

public class Session {
    private double posX;
    private double posY;

    // Getter và Setter cho vị trí của nhân vật
    public double getPosX() {
        return posX;
    }

    public void setPosX(double posX) {
        this.posX = posX;
    }

    public double getPosY() {
        return posY;
    }

    public void setPosY(double posY) {
        this.posY = posY;
    }
}
